# Обратный индекс: любая форма → ключ (лемма)
_FORM_INDEX: dict[str, str] = {}
for lemma, forms in RUSSIAN_PLURALS.items():
    for f in forms:
        _FORM_INDEX[f.lower()] = lemma

_NUMBER_NOUN_RE = re.compile(r"(\d+(?:[.,]\d+)?)(\s+)([а-яёА-ЯЁ]+)")


def plural_form(n: float | int, lemma: str) -> str:
    """Вернуть форму существительного, согласованную с числом n."""
    forms = RUSSIAN_PLURALS.get(lemma.lower())
    if not forms:
        return lemma
    # Для нецелых чисел в русском обычно используется родительный единственного:
    # "1,5 часа", "2,3 часа", "5,7 часа". Берём средний слот.
    if isinstance(n, float) and not n.is_integer():
        return forms[1]
    n_abs = abs(int(n))
    if 11 <= n_abs % 100 <= 14:
        return forms[2]
    last = n_abs % 10
    if last == 1:
        return forms[0]
    if 2 <= last <= 4:
        return forms[1]
    return forms[2]


def _parse_number(s: str) -> float:
    return float(s.replace(",", "."))


def fix_agreement(text: str) -> str:
    """Найти в тексте паттерны «<число> <слово>» и поправить слово по числу.

    Работает идемпотентно: если слово уже в нужной форме - не меняет."""

    def repl(m: re.Match[str]) -> str:
        raw_num, ws, word = m.group(1), m.group(2), m.group(3)
        lemma = _FORM_INDEX.get(word.lower())
        if not lemma:
            return m.group(0)
        try:
            n = _parse_number(raw_num)
        except ValueError:
            return m.group(0)
        correct = plural_form(n, lemma)
        # сохранить регистр первой буквы
        if word[0].isupper():
            correct = correct.capitalize()
        return f"{raw_num}{ws}{correct}"

    return _NUMBER_NOUN_RE.sub(repl, text)
